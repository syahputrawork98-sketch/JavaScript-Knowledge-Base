// C13 - Binary Logical Operators

console.log("'A' && 'B':", 'A' && 'B');
console.log("'' && 'B':", '' && 'B');

console.log("'' || 'fallback':", '' || 'fallback');
console.log("'ok' || 'fallback':", 'ok' || 'fallback');

console.log('0 ?? 10:', 0 ?? 10);
console.log('null ?? 10:', null ?? 10);
console.log('undefined ?? 10:', undefined ?? 10);
